
print("Exploring Strings - single or double quotes, result in different objects being created")

x = "I am a double quoted string"
print ("x, id(x): ", x, ", ", id(x))
y = 'I am a single quoted string'
print ("y, id(y): ", y, ", ", id(y))
x2 = "I am a double quoted string"
print("x2, id(x2): ", x2, ", ", id(x2))


